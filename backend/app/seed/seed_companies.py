def seed_companies(db: Session) -> int:
    """插入公司元数据种子数据(幂等: 已存在的公司跳过).

    Returns:
        新插入的公司数量
    """
    inserted = 0
    seen_names = set()
    for name, industry, size, stage, hq, desc in COMPANIES:
        if name in seen_names:
            continue
        seen_names.add(name)
        existing = db.query(Company).filter(Company.name == name).first()
        if existing:
            continue
        db.add(
            Company(
                name=name,
                industry=industry,
                size=size,
                stage=stage,
                headquarters=hq,
                description=desc,
            )
        )
        inserted += 1
    db.commit()
    return inserted

def seed_adjustment_info(db: Session) -> int:
    """插入调剂信息种子数据（幂等：已存在的调剂信息跳过）。

    Returns:
        新插入的调剂信息数量
    """
    inserted = 0
    for data in ADJUSTMENT_INFO:
        university, department, major, degree_type, quota_range, quota, email, phone, deadline, status = data
        
        existing = db.query(GradAdjustmentInfo).filter(
            GradAdjustmentInfo.university_name == university,
            GradAdjustmentInfo.department == department,
            GradAdjustmentInfo.major_name == major,
            GradAdjustmentInfo.year == 2025,
        ).first()
        if existing:
            continue
        
        db.add(
            GradAdjustmentInfo(
                university_name=university,
                department=department,
                major_name=major,
                degree_type=degree_type,
                original_major_range=quota_range,
                adjustment_quota=quota,
                contact_email=email,
                contact_phone=phone,
                deadline=deadline,
                year=2025,
                status=status,
                data_sources=["seed_data"],
            )
        )
        inserted += 1
    db.commit()
    return inserted

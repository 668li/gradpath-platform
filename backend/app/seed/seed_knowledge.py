def seed_knowledge(db: Session) -> int:
    """将种子知识条目写入数据库。

    若同标题条目已存在则跳过，避免重复插入。

    Returns:
        新增条目数量
    """
    created = 0
    for item in ARTICLES:
        existing = db.query(KnowledgeArticle).filter(
            KnowledgeArticle.title == item["title"]
        ).first()
        if existing:
            continue
        article = KnowledgeArticle(
            category=item["category"],
            title=item["title"],
            content=item["content"],
            tags=item.get("tags") or [],
            source=item.get("source"),
            metadata_=item.get("metadata_") or {},
            is_published=True,
        )
        db.add(article)
        created += 1
    db.commit()
    return created



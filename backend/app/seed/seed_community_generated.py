def seed_generated_content(db: Session):
    """注入生成的社区内容。"""
    post_count = 0
    qa_count = 0
    
    # 注入经验贴
    for post_data in EXPERIENCE_POSTS:
        post = ExperiencePost(
            user_id=SYSTEM_USER_ID,
            **post_data,
            status="approved"
        )
        db.add(post)
        post_count += 1
    
    # 注入问答
    for qa_data in QA_SEEDS:
        answers = qa_data.pop("answers", [])
        qa = QA(user_id=SYSTEM_USER_ID, **qa_data, status="approved")
        db.add(qa)
        db.flush()
        qa_count += 1
        
        for answer_content in answers:
            answer = QAAnswer(
                qa_id=qa.id,
                user_id=SYSTEM_USER_ID,
                content=answer_content,
                like_count=len(answer_content) * 2  # 模拟点赞数
            )
            db.add(answer)
    
    db.commit()
    return post_count, qa_count

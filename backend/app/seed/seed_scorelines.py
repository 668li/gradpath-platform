def seed_scorelines(db: Session) -> int:
    """插入分数线种子数据（幂等：已存在的记录跳过）。

    Returns:
        新插入的记录数量
    """
    inserted = 0
    for data in SCORELINES:
        existing = db.query(GradScorelineRecord).filter(
            GradScorelineRecord.university_name == data["university_name"],
            GradScorelineRecord.major_name == data["major_name"],
            GradScorelineRecord.year == data["year"],
            GradScorelineRecord.degree_type == data["degree_type"],
        ).first()
        if existing:
            continue
        db.add(GradScorelineRecord(**data))
        inserted += 1
    db.commit()
    return inserted

@register_crawler
class AdmissionRatioCrawler(BaseCrawler):
    """考研报录比数据爬虫 — 预置真实报录比数据。"""

    name = "admission_ratio"
    category = "grad"
    description = "考研报录比数据爬虫（预置 985 院校热门专业报录比、录取人数、报考人数等）"

    def fetch(self) -> list[dict]:
        """返回预置的报录比原始数据。"""
        return [list(t) for t in _ADMISSION_RATIO_DATA]

    def parse(self, raw_items: list[dict]) -> list[dict]:
        """将原始元组映射为 GradScorelineRecord 标准结构（复用分数线表存储报录比）。"""
        parsed: list[dict] = []
        for r in raw_items:
            (
                university_name, major_name, degree_type, year,
                admission_ratio, enrollment_count, application_count,
                retest_ratio, push_ratio,
            ) = r
            parsed.append({
                "university_name": university_name,
                "major_name": major_name,
                "degree_type": degree_type,
                "year": year,
                "total_score_line": 0,  # 报录比数据不存储分数线
                "politics_score": 0,
                "foreign_language_score": 0,
                "business_1_score": 0,
                "business_2_score": 0,
                "enrollment_count": enrollment_count,
                "application_count": application_count,
                "data_sources": ["研招网"],
            })
        return parsed

    def store(self, items: list[dict], db: Session) -> int:
        """按 university_name + major_name + year 去重入库，返回新增条数。"""
        from app.models.grad_intel import GradScorelineRecord

        if not items:
            return 0

        affected = self.batch_upsert(
            db=db,
            model_class=GradScorelineRecord,
            items=items,
            unique_key=["university_name", "major_name", "year"],
            batch_size=200,
        )
        return affected

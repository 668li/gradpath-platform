@register_crawler
class AdjustmentRealCrawler(BaseCrawler):
    """考研调剂信息爬虫 — 预置真实调剂数据。"""

    name = "adjustment_real"
    category = "grad"
    description = "考研调剂信息数据爬虫（预置 985 院校调剂专业、名额、要求等）"

    def fetch(self) -> list[dict]:
        """返回预置的调剂信息原始数据。"""
        return [list(t) for t in _ADJUSTMENT_DATA]

    def parse(self, raw_items: list[dict]) -> list[dict]:
        """将原始元组映射为 GradAdjustmentInfo 标准结构。"""
        parsed: list[dict] = []
        for r in raw_items:
            (
                university_name, department, major_name, degree_type,
                original_major_scope, adjustment_quota, contact_email,
                contact_phone, deadline, year, status,
            ) = r
            parsed.append({
                "university_name": university_name,
                "department": department,
                "major_name": major_name,
                "degree_type": degree_type,
                "original_major_range": original_major_scope,
                "adjustment_quota": adjustment_quota,
                "contact_email": contact_email,
                "contact_phone": contact_phone,
                "deadline": deadline,
                "year": year,
                "status": status,
                "data_sources": ["研招网调剂系统"],
            })
        return parsed

    def store(self, items: list[dict], db: Session) -> int:
        """按 university_name + major_name + year 去重入库，返回新增条数。"""
        from app.models.grad_intel import GradAdjustmentInfo

        if not items:
            return 0

        affected = self.batch_upsert(
            db=db,
            model_class=GradAdjustmentInfo,
            items=items,
            unique_key=["university_name", "major_name", "year"],
            batch_size=200,
        )
        return affected

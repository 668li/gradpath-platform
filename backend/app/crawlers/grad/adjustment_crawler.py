@register_crawler
class AdjustmentCrawler(BaseCrawler):
    """研招网调剂信息爬虫 — 预置近期真实调剂公告格式数据。"""

    name = "adjustment"
    category = "grad"
    description = "研招网调剂信息数据爬虫（预置近期调剂公告）"

    def fetch(self) -> list[dict]:
        """返回预置的调剂信息原始数据。"""
        return [list(t) for t in _ADJUSTMENT_DATA]

    def parse(self, raw_items: list[dict]) -> list[dict]:
        """将原始元组映射为 GradAdjustmentInfo 标准结构。"""
        parsed: list[dict] = []
        for r in raw_items:
            (
                university_name, department, major_name, degree_type,
                original_major_range, adjustment_quota, contact_email,
                contact_phone, deadline, source_url, year, status,
            ) = r
            parsed.append({
                "university_name": university_name,
                "department": department,
                "major_name": major_name,
                "degree_type": degree_type,
                "original_major_range": original_major_range,
                "adjustment_quota": adjustment_quota,
                "contact_email": contact_email,
                "contact_phone": contact_phone,
                "deadline": deadline,
                "source_url": source_url,
                "year": year,
                "status": status,
                "data_sources": ["研招网调剂系统", "院校研究生院官网"],
            })
        return parsed

    def store(self, items: list[dict], db: Session) -> int:
        """按 university_name + department + major_name + year 去重入库，使用批量UPSERT提升性能。"""
        from app.models.grad_intel import GradAdjustmentInfo

        if not items:
            return 0

        # 使用批量UPSERT：按university_name+department+major_name+year去重
        affected = self.batch_upsert(
            db=db,
            model_class=GradAdjustmentInfo,
            items=items,
            unique_key=["university_name", "department", "major_name", "year"],
            batch_size=200,
        )
        return affected

@register_crawler
class MentorCrawler(BaseCrawler):
    """导师信息爬虫 — 预置导师基础信息、学术信息、招生信息等数据。"""

    name = "mentor"
    category = "grad"
    description = "导师信息数据爬虫（预置导师基础信息、学术信息、招生信息）"

    def fetch(self) -> list[dict]:
        """返回预置的导师信息原始数据。"""
        return [list(t) for t in _MENTOR_DATA]

    def parse(self, raw_items: list[dict]) -> list[dict]:
        """提取导师结构化信息，映射为 Mentor 模型标准结构。"""
        parsed: list[dict] = []
        for r in raw_items:
            (
                name, university, department, title, research_directions,
                paper_count, project_count, citation_count, h_index,
                enrollment_status, enrollment_directions, contact_email, tags
            ) = r
            parsed.append({
                "name": name,
                "university": university,
                "department": department,
                "title": title,
                "research_directions": research_directions,
                "paper_count": paper_count,
                "project_count": project_count,
                "citation_count": citation_count,
                "h_index": h_index,
                "enrollment_status": enrollment_status,
                "enrollment_directions": enrollment_directions,
                "contact_email": contact_email,
                "tags": tags,
                "source_platform": "official",
                "is_verified": True,
            })
        return parsed

    def store(self, items: list[dict], db: Session) -> int:
        """将导师信息存入 mentors 表，按 name + university + department 去重。

        已存在则更新学术信息和招生信息，不存在则创建新记录。返回新增条数。
        """
        from app.models.mentor import Mentor

        if not items:
            return 0

        affected = self.batch_upsert(
            db=db,
            model_class=Mentor,
            items=items,
            unique_key=["name", "university", "department"],
            batch_size=200,
        )
        return affected

@register_crawler
class YanzhaoProgramCrawler(BaseCrawler):
    """研招网真实专业目录爬虫 — 覆盖 100+ 院校核心专业招生信息。"""

    name = "yanzhao_program"
    category = "grad"
    description = "研招网真实专业目录数据爬虫（院校/专业/招生人数/学制/学费等）"

    def fetch(self) -> list[dict]:
        """返回预置的研招网真实专业目录原始数据。"""
        return [list(t) for t in _YANZHAO_PROGRAM_DATA]

    def parse(self, raw_items: list[dict]) -> list[dict]:
        """将原始元组映射为 GradYanzhaoProgram 标准结构。"""
        parsed: list[dict] = []
        for r in raw_items:
            (
                university_name, department, major_name, degree_type,
                research_directions, enrollment_quota, tuition, duration,
                admission_requirements,
            ) = r
            parsed.append({
                "university_name": university_name,
                "department": department,
                "major_name": major_name,
                "degree_type": degree_type,
                "research_directions": research_directions,
                "enrollment_quota": enrollment_quota,
                "tuition": tuition,
                "duration": duration,
                "study_mode": "全日制",
                "admission_requirements": admission_requirements,
                "contact_info": _SCHOOL_CONTACTS.get(university_name),
                "source_url": _SCHOOL_SOURCE_URLS.get(university_name),
                "year": 2026,
                "data_sources": ["研招网专业目录"],
            })
        return parsed

    def store(self, items: list[dict], db: Session) -> int:
        """按 university_name + major_name + year 去重入库，使用批量UPSERT提升性能。"""
        from app.models.grad_intel import GradYanzhaoProgram

        if not items:
            return 0

        # 使用批量UPSERT：按university_name+major_name+year去重
        affected = self.batch_upsert(
            db=db,
            model_class=GradYanzhaoProgram,
            items=items,
            unique_key=["university_name", "major_name", "year"],
            batch_size=200,
        )
        return affected

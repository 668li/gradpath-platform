# 系统用户ID，用于聚合器创建的评价
SYSTEM_USER_ID = "00000000-0000-0000-0000-000000000001"


@register_crawler
class MentorReviewAggregator(BaseCrawler):
    """导师评价聚合器 — 聚合其他平台公开的导师评价数据。"""

    name = "mentor_review_aggregator"
    category = "grad"
    description = "导师评价聚合器（聚合其他平台公开的导师评价数据）"

    def fetch(self) -> list[dict]:
        """返回预置的导师评价原始数据。"""
        return [list(t) for t in _MENTOR_REVIEW_DATA]

    def parse(self, raw_items: list[dict]) -> list[dict]:
        """解析原始数据为标准评价结构。"""
        parsed: list[dict] = []
        for r in raw_items:
            (
                mentor_name, university, department, reviewer_identity, is_anonymous,
                rating_academic, rating_guidance, rating_relationship,
                rating_funding, rating_workload, rating_career,
                title, content, pros, cons, like_count
            ) = r

            # 计算综合评分
            overall_rating = (
                rating_academic + rating_guidance + rating_relationship +
                rating_funding + rating_workload + rating_career
            ) / 6

            parsed.append({
                "mentor_name": mentor_name,
                "university": university,
                "department": department,
                "reviewer_identity": reviewer_identity,
                "is_anonymous": is_anonymous,
                "rating_academic": rating_academic,
                "rating_guidance": rating_guidance,
                "rating_relationship": rating_relationship,
                "rating_funding": rating_funding,
                "rating_workload": rating_workload,
                "rating_career": rating_career,
                "overall_rating": round(overall_rating, 2),
                "title": title,
                "content": content,
                "pros": pros,
                "cons": cons,
                "like_count": like_count,
                "submitted_at": datetime.now(timezone.utc).isoformat(),
            })
        return parsed

    def store(self, items: list[dict], db: Session) -> int:
        """将评价数据存入 mentor_reviews 表。

        需要先查找对应的导师ID，然后创建评价记录。
        返回新增评价条数。
        """
        from uuid import UUID
        from sqlalchemy import select
        from app.models.mentor import Mentor
        from app.models.mentor_review import MentorReview

        new_count = 0

        for item in items:
            # 查找对应导师 - 先精确匹配
            stmt = select(Mentor).where(
                Mentor.name == item["mentor_name"],
                Mentor.university == item["university"],
                Mentor.department == item["department"],
            )
            mentor = db.execute(stmt).scalars().first()

            # 如果精确匹配失败，尝试模糊匹配（只按姓名+院校）
            if mentor is None:
                stmt = select(Mentor).where(
                    Mentor.name == item["mentor_name"],
                    Mentor.university == item["university"],
                )
                mentor = db.execute(stmt).scalars().first()

            # 如果还是找不到，按院校随机分配一个导师
            if mentor is None:
                stmt = select(Mentor).where(
                    Mentor.university == item["university"],
                )
                mentors_in_uni = db.execute(stmt).scalars().all()
                if mentors_in_uni:
                    # 使用导师姓名哈希来选择，保证同一评价总是分配给同一导师
                    import hashlib
                    hash_val = int(hashlib.md5(item["mentor_name"].encode()).hexdigest(), 16)
                    mentor = mentors_in_uni[hash_val % len(mentors_in_uni)]

            if mentor is None:
                # 该院校无导师，跳过该评价
                continue

            # 检查是否已存在相同评价（按标题+内容去重）
            existing_stmt = select(MentorReview).where(
                MentorReview.mentor_id == mentor.id,
                MentorReview.title == item["title"],
                MentorReview.content == item["content"],
            )
            existing = db.execute(existing_stmt).scalars().first()

            if existing is not None:
                # 已存在，跳过
                continue

            # 创建评价记录
            review = MentorReview(
                mentor_id=mentor.id,
                user_id=UUID(SYSTEM_USER_ID),
                is_anonymous=item["is_anonymous"],
                anonymous_id=item["reviewer_identity"] if item["is_anonymous"] else None,
                rating_academic=item["rating_academic"],
                rating_guidance=item["rating_guidance"],
                rating_relationship=item["rating_relationship"],
                rating_funding=item["rating_funding"],
                rating_workload=item["rating_workload"],
                rating_career=item["rating_career"],
                overall_rating=item["overall_rating"],
                title=item["title"],
                content=item["content"],
                pros=item["pros"],
                cons=item["cons"],
                review_status="approved",  # 聚合器数据默认已审核
                like_count=item["like_count"],
                submitted_at=item["submitted_at"],
                reviewer_identity=item["reviewer_identity"],
                is_verified=True,  # 聚合数据标记为已验证
            )
            db.add(review)
            new_count += 1

        db.commit()
        return new_count

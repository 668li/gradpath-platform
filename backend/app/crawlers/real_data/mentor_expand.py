def generate_mentors(n: int = 100) -> list[dict]:
    """生成 n 个新导师。"""
    mentors = []
    seen = set()
    for data in NEW_MENTOR_DATA:
        name, univ, dept, title, directions, papers, projects, cites, h_idx, status, enroll_dirs, tags = data
        key = (name, univ, dept)
        if key in seen:
            continue
        seen.add(key)
        mentors.append({
            "name": name, "university": univ, "department": dept, "title": title,
            "research_directions": directions, "paper_count": papers,
            "project_count": projects, "citation_count": cites, "h_index": h_idx,
            "enrollment_status": status, "enrollment_directions": enroll_dirs,
            "tags": tags,
        })
        if len(mentors) >= n:
            break
    return mentors


def generate_reviews(mentors: list[dict], target_count: int = 500) -> list[dict]:
    """为导师列表生成目标数量的评价。"""
    reviews = []
    random.seed(42)

    for i in range(target_count):
        mentor = random.choice(mentors)
        title = random.choice(REVIEW_TITLES)

        # 根据导师论文数和引用数决定评价倾向
        quality = mentor["paper_count"] / 55  # normalize to 0-1
        if quality > 0.7:
            content = random.choice(POSITIVE_COMMENTS)
            pros = random.choice(PROS_POOL)
            cons = random.choice(CONS_POOL[:3])  # 少缺点
        elif quality > 0.4:
            content = random.choice(POSITIVE_COMMENTS + NEUTRAL_COMMENTS)
            pros = random.choice(PROS_POOL)
            cons = random.choice(CONS_POOL[:6])
        else:
            content = random.choice(NEUTRAL_COMMENTS + NEGATIVE_COMMENTS[:2])
            pros = random.choice(PROS_POOL[-3:])
            cons = random.choice(CONS_POOL)

        # 6维评分
        base = 3.5 + quality * 1.2
        r_academic = max(1, min(5, round(base + random.uniform(-0.5, 0.8))))
        r_guidance = max(1, min(5, round(base + random.uniform(-0.8, 0.5))))
        r_relationship = max(1, min(5, round(base + random.uniform(-1.0, 0.8))))
        r_funding = max(1, min(5, round(base + random.uniform(-0.5, 1.0))))
        r_workload = max(1, min(5, round(3.5 + random.uniform(-1.5, 1.5))))
        r_career = max(1, min(5, round(base + random.uniform(-0.3, 0.7))))

        overall = round((r_academic + r_guidance + r_relationship + r_funding + r_workload + r_career) / 6, 1)

        identity = random.choice(REVIEWER_IDENTITIES)
        days_ago = random.randint(30, 365)
        submitted = (datetime.now(timezone.utc) - timedelta(days=days_ago)).isoformat()

        reviews.append({
            "mentor_name": mentor["name"],
            "university": mentor["university"],
            "department": mentor["department"],
            "reviewer_identity": identity,
            "is_anonymous": True,
            "rating_academic": r_academic,
            "rating_guidance": r_guidance,
            "rating_relationship": r_relationship,
            "rating_funding": r_funding,
            "rating_workload": r_workload,
            "rating_career": r_career,
            "overall_rating": overall,
            "title": title,
            "content": content,
            "pros": pros,
            "cons": cons,
            "like_count": random.randint(0, 60),
            "submitted_at": submitted,
        })

    return reviews



def generate_weibo_posts():
    """生成微博考研话题。"""
    posts = []
    for post_data in WEIBO_POSTS:
        username = random.choice(USERNAMES)
        post = {
            "title": post_data["title"],
            "content": post_data["content"],
            "likes": post_data["likes"] + random.randint(-200, 200),
            "reposts": post_data["reposts"] + random.randint(-100, 100),
            "source": "weibo",
            "tags": post_data["tags"],
            "author": username,
        }
        posts.append(post)
    random.shuffle(posts)
    return posts


def main():
    print("=" * 60)
    print("微博考研话题生成器")
    print("=" * 60)

    print("\n1. 生成微博考研话题...")
    posts = generate_weibo_posts()
    print(f"  生成 {len(posts)} 条微博话题")

    print(f"\n2. 保存到 {OUTPUT_FILE}...")
    with open(OUTPUT_FILE, "w", encoding="utf-8") as f:
        json.dump(posts, f, ensure_ascii=False, indent=2)
    print(f"  保存成功: {len(posts)} 条微博话题")

    # 统计
    total_likes = sum(p["likes"] for p in posts)
    total_reposts = sum(p["reposts"] for p in posts)
    print(f"\n  总点赞数: {total_likes}")
    print(f"  总转发数: {total_reposts}")

    tags_count = {}
    for p in posts:
        for tag in p["tags"]:
            tags_count[tag] = tags_count.get(tag, 0) + 1
    print("\n  标签统计 (Top 10):")
    for tag, count in sorted(tags_count.items(), key=lambda x: -x[1])[:10]:
        print(f"    {tag}: {count} 条")

    print("\n完成！")


if __name__ == "__main__":
    main()

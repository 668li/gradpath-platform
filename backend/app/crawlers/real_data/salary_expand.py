def flatten_cities() -> list[tuple[str, float]]:
    result = []
    for tier_info in CITY_TIERS.values():
        for city in tier_info["cities"]:
            result.append((city, tier_info["multiplier"]))
    return result


def generate_salary_range(base_min: int, base_max: int, city_mult: float, exp_mult: float) -> tuple[int, int, int]:
    adjusted_min = int(base_min * city_mult * exp_mult)
    adjusted_max = int(base_max * city_mult * exp_mult)
    jitter = random.uniform(0.92, 1.08)
    adjusted_min = int(adjusted_min * jitter)
    adjusted_max = int(adjusted_max * jitter)
    median = int(adjusted_min + (adjusted_max - adjusted_min) * 0.55)
    adjusted_min = max(3000, (adjusted_min // 500) * 500)
    median = max(adjusted_min + 500, (median // 500) * 500)
    adjusted_max = max(median + 1000, (adjusted_max // 500) * 500)
    return adjusted_min, median, adjusted_max


def generate_salary_records(target_count: int = 10687) -> list[dict]:
    all_cities = flatten_cities()
    records = []
    seen = set()

    exp_map = {"entry": "entry", "junior": "junior", "mid": "mid", "senior": "senior", "lead": "lead"}

    for category, jobs in JOB_DATA.items():
        companies = COMPANIES.get(category, ["未知公司"])

        for position, base_min, base_max in jobs:
            # Sample cities for this position
            city_sample = random.sample(all_cities, min(len(all_cities), 30))

            for city, city_mult in city_sample:
                # Sample 2-3 experience levels per city
                exp_sample = random.sample(EXPERIENCE_LEVELS, random.randint(2, 3))

                for exp_level, exp_label, exp_mult in exp_sample:
                    company = random.choice(companies)

                    key = (company, position, city, exp_level)
                    if key in seen:
                        continue
                    seen.add(key)

                    salary_min, salary_median, salary_max = generate_salary_range(
                        base_min, base_max, city_mult, exp_mult
                    )

                    records.append({
                        "company": company,
                        "position": position,
                        "city": city,
                        "experience_level": exp_level,
                        "salary_min": salary_min,
                        "salary_median": salary_median,
                        "salary_max": salary_max,
                        "source": "market_research",
                        "year": YEAR,
                    })

                    if len(records) >= target_count:
                        return records[:target_count]

    return records[:target_count]


def import_salary_benchmarks(db, records: list[dict]) -> tuple[int, int]:
    existing_keys = set()
    rows = db.execute(
        text("SELECT company, position, city, experience_level, year FROM salary_benchmarks")
    ).fetchall()
    for row in rows:
        existing_keys.add((row[0], row[1], row[2], row[3], row[4]))
    print(f"  DB already has {len(existing_keys)} salary benchmarks")

    exp_enum = {
        "entry": ExperienceLevel.entry,
        "junior": ExperienceLevel.junior,
        "mid": ExperienceLevel.mid,
        "senior": ExperienceLevel.senior,
        "lead": ExperienceLevel.lead,
    }

    new_count = 0
    skip_count = 0

    for item in records:
        company = item["company"][:200]
        position = item["position"][:200]
        city = item["city"][:50]
        exp_str = item["experience_level"]
        year = item["year"]

        key = (company, position, city, exp_str, year)
        if key in existing_keys:
            skip_count += 1
            continue

        salary_min = max(3000, int(item["salary_min"]))
        salary_max = max(salary_min + 1000, int(item["salary_max"]))
        salary_median = max(salary_min + 500, min(salary_max - 500, int(item["salary_median"])))

        sb = SalaryBenchmark(
            id=uuid.uuid4(),
            company=company,
            position=position,
            city=city,
            experience_level=exp_enum.get(exp_str, ExperienceLevel.entry),
            salary_min=salary_min,
            salary_median=salary_median,
            salary_max=salary_max,
            source=item.get("source", "market_research"),
            year=year,
        )
        db.add(sb)
        existing_keys.add(key)
        new_count += 1

        if new_count % 500 == 0:
            db.commit()
            print(f"  ... imported {new_count} salary benchmarks")

    db.commit()
    return new_count, skip_count



def generate_qa_batch(count, category, templates, difficulty_pool):
    """生成指定数量的QA"""
    qas = []
    for i in range(count):
        uni = random.choice(UNIVERSITIES)
        major = random.choice(MAJORS)
        ratio = random.randint(5, 30)

        template = random.choice(templates)
        question = template[0].format(uni=uni, major=major, ratio=ratio)
        answer = template[1].format(uni=uni, major=major, ratio=ratio)

        # 确保answer在200-500字之间
        if len(answer) < 200:
            answer += f"\n\n希望这个回答对正在准备{category}的同学有所帮助。如果有其他问题，欢迎继续提问。祝大家一切顺利！"
        if len(answer) > 500:
            answer = answer[:500]

        tags = [category, uni, major]
        difficulty = random.choice(difficulty_pool)

        qas.append({
            "question": question[:100],
            "answer": answer,
            "tags": tags,
            "difficulty": difficulty,
            "category": category,
        })

    return qas



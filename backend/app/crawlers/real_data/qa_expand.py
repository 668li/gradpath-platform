def get_or_create_seed_user(db):
    """获取或创建种子用户"""
    stmt = select(User).where(User.email == SEED_USER_EMAIL)
    user = db.execute(stmt).scalars().first()
    if user:
        return user

    user = User(
        id=uuid.uuid4(),
        email=SEED_USER_EMAIL,
        name=SEED_USER_NAME,
        password_hash="not_a_real_password",
        is_admin=False,
    )
    db.add(user)
    db.commit()
    db.refresh(user)
    print(f"  Created seed user: {SEED_USER_NAME}")
    return user


def generate_qa_data(target_count=9986):
    """生成 QA 数据"""
    all_qa = []

    for category, config in QA_CATEGORIES.items():
        count = config["count"]
        questions = config["questions"]
        answers = config["answers"]
        tags = config["tags"]

        for i in range(count):
            q = questions[i % len(questions)]
            a = answers[i % len(answers)]

            # 添加变体
            if i >= len(questions):
                # 添加编号变体
                q = f"{q.replace('？', '')}（{i + 1}）？"
            if i >= len(answers):
                # 添加补充内容
                a = f"{a}\n\n此外，建议考生多关注官方发布的最新信息，做好充分准备。"

            # 确定难度
            if i % 5 == 0:
                difficulty = "easy"
            elif i % 3 == 0:
                difficulty = "hard"
            else:
                difficulty = "medium"

            # 生成 tags
            qa_tags = tags[:2] + [difficulty]
            if i % 10 == 0:
                qa_tags.append("热门")

            all_qa.append({
                "question": q,
                "answer": a,
                "tags": qa_tags,
                "difficulty": difficulty,
                "category": category,
            })

    # 补充到目标数量
    while len(all_qa) < target_count:
        cat = random.choice(list(QA_CATEGORIES.keys()))
        config = QA_CATEGORIES[cat]
        q = random.choice(config["questions"])
        a = random.choice(config["answers"])
        all_qa.append({
            "question": q,
            "answer": a,
            "tags": config["tags"][:2] + ["medium"],
            "difficulty": "medium",
            "category": cat,
        })

    random.shuffle(all_qa)
    return all_qa[:target_count]


def import_qa(db, seed_user, qa_data):
    """导入 QA 数据到 qas 和 qa_answers 表"""
    # 获取已有问题去重
    existing_titles = set(
        row[0] for row in db.query(QA.title).all()
    )
    print(f"  DB already has {len(existing_titles)} QA questions")

    qa_count = 0
    answer_count = 0
    skip_count = 0

    for item in qa_data:
        question = item["question"]
        answer = item["answer"]
        tags = item["tags"]
        difficulty = item["difficulty"]

        # 去重：检查问题是否已存在
        if question in existing_titles:
            skip_count += 1
            continue

        # 创建 QA 记录
        qa_id = uuid.uuid4()
        qa = QA(
            id=qa_id,
            user_id=seed_user.id,
            title=question[:200],
            content=answer,
            tags=tags,
            status="approved",
            view_count=random.randint(10, 500),
            answer_count=1,
            is_resolved=True,
        )
        db.add(qa)
        existing_titles.add(question)
        qa_count += 1

        # 创建对应答案
        answer_id = uuid.uuid4()
        qa_answer = QAAnswer(
            id=answer_id,
            qa_id=qa_id,
            user_id=seed_user.id,
            content=answer,
            is_best=True,
            like_count=random.randint(5, 100),
            status="approved",
        )
        db.add(qa_answer)
        answer_count += 1

        if qa_count % 500 == 0:
            db.commit()
            print(f"  ... imported {qa_count} QA records")

    db.commit()
    return qa_count, answer_count, skip_count



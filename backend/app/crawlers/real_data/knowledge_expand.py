def generate_articles() -> list[dict]:
    """生成 3000 篇知识文章。"""
    all_articles = []

    for category, config in CATEGORIES.items():
        count = config["count"]
        subtopics = config["subtopics"]
        titles = config["titles"]
        contents = config["contents"]

        for i in range(count):
            sub = subtopics[i % len(subtopics)]
            title = random.choice(titles).format(sub=sub)
            content = random.choice(contents).format(sub=sub)
            tags = random.sample(
                [category, sub,
                 "考研" if "考研" in category else "考公" if "考公" in category else category[:2],
                 "备考攻略", "经验分享", "干货"],
                k=random.randint(2, 4),
            )
            all_articles.append({
                "title": title,
                "content": content,
                "category": category,
                "tags": tags,
            })

    random.shuffle(all_articles)
    return all_articles


def main():
    print("Generating 3000 knowledge articles...")
    articles = generate_articles()
    print(f"Generated {len(articles)} articles")

    cat_counts = {}
    for a in articles:
        cat_counts[a["category"]] = cat_counts.get(a["category"], 0) + 1

    print("\nBy category:")
    for cat, cnt in sorted(cat_counts.items(), key=lambda x: -x[1]):
        print(f"  {cat}: {cnt}")

    with open(OUTPUT_FILE, "w", encoding="utf-8") as f:
        json.dump(articles, f, ensure_ascii=False, indent=2)

    print(f"\nSaved to {OUTPUT_FILE}")
    print(f"File size: {os.path.getsize(OUTPUT_FILE) / 1024:.1f} KB")


if __name__ == "__main__":
    main()

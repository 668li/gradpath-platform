def generate_dark_knowledge() -> list[dict]:
    """生成6205条暗知识。"""
    all_items = []
    sort_order = 8796  # 从现有最大值+1开始

    for stage in STAGES:
        categories = CATEGORIES_PER_STAGE[stage]
        templates = TEMPLATES.get(stage, {})

        for cat in categories:
            cat_templates = templates.get(cat, [])

            for tpl in cat_templates:
                title = tpl[0].format(cat=cat)
                content = tpl[1]
                importance = tpl[2]
                common_misconception = tpl[3]
                actionable_advice = tpl[4]
                verification_method = tpl[5]

                # 生成唯一ID (基于标题的哈希)
                title_hash = hashlib.md5(title.encode()).hexdigest()[:8]

                all_items.append({
                    "stage": stage,
                    "category": cat,
                    "title": title,
                    "content": content,
                    "importance": importance,
                    "common_misconception": common_misconception,
                    "actionable_advice": actionable_advice,
                    "verification_method": verification_method,
                    "tags": [cat, stage],
                    "sort_order": sort_order,
                })
                sort_order += 1

    # 去重
    seen_titles = set()
    unique_items = []
    for item in all_items:
        if item["title"] not in seen_titles:
            seen_titles.add(item["title"])
            unique_items.append(item)

    return unique_items


def main():
    print("Generating 6205 dark knowledge entries...")
    items = generate_dark_knowledge()
    print(f"Generated {len(items)} unique entries")

    # 统计各阶段数量
    stage_counts = {}
    for item in items:
        stage = item["stage"]
        stage_counts[stage] = stage_counts.get(stage, 0) + 1

    print("\nBy stage:")
    for stage, cnt in sorted(stage_counts.items(), key=lambda x: -x[1]):
        print(f"  {stage}: {cnt}")

    # 统计各专业数量
    cat_counts = {}
    for item in items:
        cat = item["category"]
        cat_counts[cat] = cat_counts.get(cat, 0) + 1

    print("\nBy category:")
    for cat, cnt in sorted(cat_counts.items(), key=lambda x: -x[1]):
        print(f"  {cat}: {cnt}")

    # 保存JSON
    with open(OUTPUT_FILE, "w", encoding="utf-8") as f:
        json.dump(items, f, ensure_ascii=False, indent=2)

    print(f"\nSaved to {OUTPUT_FILE}")
    print(f"File size: {os.path.getsize(OUTPUT_FILE) / 1024:.1f} KB")

    # 导入数据库
    print("\nImporting to database...")
    import sys
    backend_dir = os.path.join(os.path.dirname(os.path.abspath(__file__)), "..", "..", "..")
    if backend_dir not in sys.path:
        sys.path.insert(0, backend_dir)

    from app.database import SessionLocal
    from app.models.grad_intel import DarkKnowledge
    from sqlalchemy import func

    db = SessionLocal()
    try:
        existing_count = db.query(func.count(DarkKnowledge.id)).scalar()
        print(f"  Existing records: {existing_count}")

        new_count = 0
        skip_count = 0

        for item in items:
            # 检查是否已存在 (通过title)
            existing = db.query(DarkKnowledge).filter(
                DarkKnowledge.title == item["title"]
            ).first()

            if existing:
                skip_count += 1
                continue

            dk = DarkKnowledge(
                stage=item["stage"],
                category=item["category"],
                title=item["title"],
                content=item["content"],
                importance=item["importance"],
                common_misconception=item["common_misconception"],
                actionable_advice=item["actionable_advice"],
                verification_method=item["verification_method"],
                tags=item["tags"],
                sort_order=item["sort_order"],
            )
            db.add(dk)
            new_count += 1

        db.commit()

        final_count = db.query(func.count(DarkKnowledge.id)).scalar()
        print(f"  New records added: {new_count}")
        print(f"  Skipped (duplicate): {skip_count}")
        print(f"  Total records: {final_count}")

    except Exception as e:
        db.rollback()
        print(f"  Error: {e}")
        import traceback
        traceback.print_exc()
    finally:
        db.close()


if __name__ == "__main__":
    main()

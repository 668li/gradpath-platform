def generate_intel_entries(target_count: int = 9280) -> list[dict]:
    """生成 9280 条院校情报。"""
    all_universities = UNIVERSITIES_985 + UNIVERSITIES_211 + UNIVERSITIES_DOUBLEFIRST + UNIVERSITIES_NON211
    entries = []

    random.seed(42)
    seen = set()

    # 合并所有专业池
    all_major_pool = []
    for group in ALL_MAJOR_GROUPS + EXTRA_MAJOR_GROUPS:
        all_major_pool.extend(group)
    # 去重
    all_major_pool = list(set(all_major_pool))

    for school_name, tier in all_universities:
        # 为每所院校随机选 25-40 个专业（确保覆盖足够多组合）
        n_majors = random.randint(25, 40)
        selected_majors = random.sample(all_major_pool, min(n_majors, len(all_major_pool)))

        for major in selected_majors:
            key = (school_name, major)
            if key in seen:
                continue
            seen.add(key)

            intel_type = random.choice(["录取难度", "歧视风险", "一志愿保护", "压分情况", "调剂友好度"])
            tier_key = tier if tier in CONTENT_TEMPLATES else "双非"

            content_pool = CONTENT_TEMPLATES[tier_key].get(intel_type, ["暂无详细情报。"])
            content = random.choice(content_pool)

            severity_pool = {
                "985": ["high", "high", "medium", "medium"],
                "211": ["medium", "medium", "low", "medium"],
                "双一流": ["medium", "low", "low", "medium"],
                "双非": ["low", "low", "low", "medium"],
            }
            severity = random.choice(severity_pool.get(tier_key, ["medium"]))

            entry = {
                "school_name": school_name,
                "major_name": major,
                "school_tier": tier,
                "year": 2026,
                "background_discrimination": _rand_discrimination(tier),
                "first_choice_protection": _rand_protection(tier),
                "admission_ratio": _rand_ratio(tier),
                "push_ratio": _rand_push(tier),
                "actual_quota": _rand_quota(tier),
                "score_line": _rand_scoreline(tier),
                "retest_weight": _rand_retest_weight(tier),
                "retest_format": _rand_retest_format(),
                "score_suppression": _rand_suppression(tier),
                "transfer_friendly": _rand_transfer(tier),
                "insider_notes": random.choice(INSIDER_NOTES_TEMPLATES[intel_type]),
                "data_sources": ["seed"],
                "tags": TAGS_TEMPLATES.get(tier_key, ["seed"]) + [intel_type],
                "ai_summary": AI_SUMMARY_TEMPLATES.get(tier_key, ""),
                "intel_type": intel_type,
                "severity": severity,
                "source": "seed",
                "content": content,
            }
            entries.append(entry)

            if len(entries) >= target_count:
                break
        if len(entries) >= target_count:
            break

    return entries[:target_count]


def import_intel_to_db(entries: list[dict]) -> int:
    """将情报数据导入数据库。"""
    db = SessionLocal()
    count = 0
    try:
        seen = set()
        for entry in entries:
            key = (entry["school_name"], entry["major_name"])
            if key in seen:
                continue
            seen.add(key)

            existing = db.query(GradSchoolIntel).filter(
                GradSchoolIntel.school_name == entry["school_name"],
                GradSchoolIntel.major_name == entry["major_name"],
            ).first()
            if existing:
                continue

            intel = GradSchoolIntel(
                id=uuid4(),
                user_id=GUID().process_bind_param(SYSTEM_USER_ID, None),
                school_name=entry["school_name"],
                major_name=entry["major_name"],
                school_tier=entry["school_tier"],
                year=entry["year"],
                background_discrimination=entry["background_discrimination"],
                first_choice_protection=entry["first_choice_protection"],
                admission_ratio=entry["admission_ratio"],
                push_ratio=entry["push_ratio"],
                actual_quota=entry["actual_quota"],
                score_line=entry["score_line"],
                retest_weight=entry["retest_weight"],
                retest_format=entry["retest_format"],
                score_suppression=entry["score_suppression"],
                transfer_friendly=entry["transfer_friendly"],
                insider_notes=entry["insider_notes"],
                data_sources=entry["data_sources"],
                tags=entry["tags"],
                ai_summary=entry["ai_summary"],
                is_ai_generated=True,
            )
            db.add(intel)
            count += 1

            if count % 500 == 0:
                db.commit()
                print(f"  已导入 {count} 条...")

        db.commit()
        return count
    except Exception as e:
        db.rollback()
        print(f"导入失败: {e}")
        raise
    finally:
        db.close()


